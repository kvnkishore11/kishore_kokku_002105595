private void populateTable() {
    
    DefaultTableModel model = (DefaultTableModel) tblAllCars.getModel();
    model.setRowCount(0);

    for (Car car : allCars.getAllCars()){

        Object[] row = new Object[8];
        row[0] = car ;
        row[1] = car.getModelName();
        row[2] = car.getYearOfManufacture();
        row[3] = car.getNoOfSeats();
        row[4] = car.getSerialNumber();
        row[5] = car.getLocation();
        row[6] = car.getMaintenaceCertificate();
        row[7] = car.getAvailability();
        model.addRow(row);

    }


    }
